TEST( CFPP_String, OperatorSubscript )
{
    CF::String s( "hello, world" );
    
    ASSERT_EQ( s[  0 ], 'h' );
    ASSERT_EQ( s[ 11 ], 'd' );
    
    ASSERT_NO_THROW( s[  12 ] );
    ASSERT_NO_THROW( s[ 100 ] );
    
    ASSERT_NO_FATAL_FAILURE( s[  12 ] );
    ASSERT_NO_FATAL_FAILURE( s[ 100 ] );
    
    ASSERT_EQ( s[  12 ], 0 );
    ASSERT_EQ( s[ 100 ], 0 );
}

