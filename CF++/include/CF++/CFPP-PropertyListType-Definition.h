#ifndef CFPP_PROPERTY_LIST_TYPE_DEFINITION_H
#define CFPP_PROPERTY_LIST_TYPE_DEFINITION_H

#include <CF++/CFPP-PropertyListType.h>
#include <CF++/CFPP-Data.h>
#include <CF++/CFPP-URL.h>

namespace CF
{
    template < class T >
    T PropertyListType< T >::FromPropertyList( std::string path )
    {
        T                object;
        Data             data;
        URL              url;
        CFReadStreamRef  cfStream;
        Data::Byte     * bytes;
        CFIndex          i;
        CFTypeRef        cfObject;
        
        url      = URL::FileSystemURL( path );
        cfStream = CFReadStreamCreateWithFile( static_cast< CFAllocatorRef >( NULL ), url );
        
        if( cfStream == NULL )
        {
            return object;
        }
        
        if( CFReadStreamOpen( cfStream ) == false )
        {
            CFRelease( cfStream );
            
            return object;
        }
        
        bytes = new Data::Byte[ 4096 ];
        
        if( bytes == NULL )
        {
            CFRelease( cfStream );
            
            return object;
        }
        
        while( CFReadStreamHasBytesAvailable( cfStream ) )
        {
            i = CFReadStreamRead( cfStream, bytes, 4096 );
            
            data.AppendBytes( bytes, i );
        }
        
        delete[] bytes;
        
        CFReadStreamClose( cfStream );
        CFRelease( cfStream );
        
        cfObject = CFPropertyListCreateWithData( static_cast< CFAllocatorRef >( NULL ), data, 0, NULL, NULL );
        
        if( cfObject != NULL )
        {
            object = cfObject;

            CFRelease( cfObject );
        }
        
        return object;
    }
    
    template < class T >
    bool PropertyListType< T >::ToPropertyList( std::string path, PropertyListFormat format ) const
    {
        URL              url;
        Data             d;
        CFWriteStreamRef cfStream;
        
        if( this->IsValid() == false )
        {
            return false;
        }
        
        url = URL::FileSystemURL( path );
        d   = this->ToPropertyList( format );
        
        if( d.GetLength() == 0 )
        {
            return false;
        }
        
        cfStream = CFWriteStreamCreateWithFile( static_cast< CFAllocatorRef >( NULL ), url );
        
        if( cfStream == NULL )
        {
            return false;
        }
        
        if( CFWriteStreamOpen( cfStream ) == false )
        {
            CFRelease( cfStream );
            
            return false;
        }
        
        CFWriteStreamWrite( cfStream, d.GetBytePtr(), d.GetLength() );
        CFWriteStreamClose( cfStream );
        CFRelease( cfStream );
        
        return true;
    }
    
    template < class T >
    Data PropertyListType< T >::ToPropertyList( PropertyListFormat format ) const
    {
        CFDataRef            cfData;
        CFErrorRef           cfError;
        CFPropertyListFormat cfFormat;
        Data                 d;
        
        if( this->IsValid() == false )
        {
            return d;
        }
        
        cfError  = NULL;
        cfFormat = kCFPropertyListXMLFormat_v1_0;
        
        if( format == PropertyListFormatBinary )
        {
            cfFormat = kCFPropertyListBinaryFormat_v1_0;
        }
        
        cfData = CFPropertyListCreateData( static_cast< CFAllocatorRef >( NULL ), this->GetCFObject(), cfFormat, 0, &cfError );
        
        if( cfError != NULL )
        {
            if( cfData != NULL )
            {
                CFRelease( cfData );
            }
            
            return d;
        }
        
        if( cfData != NULL )
        {
            d = cfData;
            
            CFRelease( cfData );
        }
        
        return d;
    }
}

#endif /* CFPP_PROPERTY_LIST_TYPE_DEFINITION_H */
